/**
 * i18n - Simple translation system
 */

// Sync with LocaleContext
export function getLocale() {
  try {
    const stored = localStorage.getItem('preferred_locale');
    if (stored && ['zh', 'ja', 'en'].includes(stored)) return stored;
  } catch {}
  return 'zh';
}

export function setLocale(locale) {
  try {
    localStorage.setItem('preferred_locale', locale);
    window.dispatchEvent(new CustomEvent('localeChanged', { detail: { locale } }));
    // Reload page to apply language change
    window.location.reload();
  } catch {}
}

export function t(zhText, locale = 'zh') {
  if (!zhText) return '';
  if (!translations.zh[zhText]) {
    // Auto-register new Chinese text
    translations.zh[zhText] = zhText;
    translations.ja[zhText] = zhText;
    translations.en[zhText] = zhText;
  }
  return translations[locale]?.[zhText] || translations.zh[zhText] || zhText;
}

